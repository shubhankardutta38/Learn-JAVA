public class assignmentoperators {
    public static void main(String[] args) {
        int a = 4;
        int var;// 4 bytes
        var = a;
        System.out.println("Var using =" + var);
        var += a;// shortent operators var=var+a var=4+4=8
        System.out.println("Var Using + =" + var);
        var *= a;// var=var*a var=8*4=32
        System.out.println("var using * =" + var);
        var -= a;// var=var*a var=8*4=32
        System.out.println("var using * =" + var);
    }
}
